#ifndef LOGGING_H
#define LOGGING_H

#include <stdbool.h>

typedef enum
{
    LOG_LEVEL_FATAL = 0,
    LOG_LEVEL_ERROR = 1,
    LOG_LEVEL_WARN = 2,
    LOG_LEVEL_INFO = 3,
    LOG_LEVEL_DEBUG = 4,
    LOG_LEVEL_TRACE = 5,
} logging_level_t;

typedef struct
{
    logging_level_t level;
    bool timestamp;
    bool human_readable_timestamp;
} logging_settings_t;

int logging_initialize(logging_settings_t *settings);

int log_message(logging_level_t level, char *format, ...);

#endif // LOGGING_H

